import string

# constantes do modulo string

print(string.ascii_lowercase)
# todas as letras do alfabeto em minúsculas
print()

print(string.ascii_uppercase)
# todas as letras do alfabeto em maiúsculas
print()

print(string.ascii_letters)
# todas as letras do alfabeto em minúsculas e maiúsculas
print()

print(string.digits)
# todos os números de 0 a 9 em string
print()

print(string.hexdigits)
# todos os caracteres hexadecimais em string
print()

print(string.octdigits)
# a string '01234567'
print()

print(string.punctuation)
# todos os caracteres de pontuação
print()

print(string.printable)
# string de caracteres que são considerados imprimíveis. 
# Esta é uma combinação de digits, ascii_letters, punctuation e whitespace
print()

print(string.whitespace)
print()

# O python é uma liguagem interpretada,
# o interpretador lé o código da esquerda para a direita de cima para baixo

print("ola 'meu' nome é 'bruno' ") # como colocar aspas dentro de uma string
print('ola "meu" nome é "bruno" ')
# outra forma

print("ola meu nome é \"bruno\", prazer.") # \ é uma caractere de escape
print(r"ola meu nome é \"bruno\", prazer.")
# o r no começo impede que códigos sejam executados dentro da string